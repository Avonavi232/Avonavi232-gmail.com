export const genData = (initial: number, n: number, from: number) => {
  const data = [];

  data.push(from === 0 ? initial : null);

  for (let i = 0; i < n - 1; i++) {
    if (i < from) {
      data.push(null);
      continue;
    }
    const rand = Math.sign(Math.random() - 0.5);
    const part = initial * 0.06;
    // @ts-ignore
    const next = (data[data.length - 1] || initial) + rand * part;
    data.push(Math.round(next));
  }

  return data;
};

export const genLabels = (lastDate: string, n: number) => {
  const date = new Date(lastDate);
  const labels = [+date];

  for (let i = 0; i < n - 1; i++) {
    const newDate = labels[labels.length - 1] - dayInMs;

    labels.push(newDate);
  }

  return labels.reverse();
};

